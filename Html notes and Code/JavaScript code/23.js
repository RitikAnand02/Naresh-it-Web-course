// use switch to print the days of week by number in console...

let num = 8;
switch (num) {
  case 0:
    console.log("sun");
    break;
  case 1:
    console.log("mon");
    break;
  case 2:
    console.log("tues");
    break;
  case 3:
    console.log("wed");
    break;
  case 4:
    console.log("thur");
    break;
  case 5:
    console.log("fri");
    break;
  case 6:
    console.log("sat");
    break;
  default:
    console.log("invalid");
}
